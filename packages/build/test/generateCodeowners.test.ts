import { describe, expect, it } from 'bun:test';
import { defineOwnheim } from '@ownheim/core/defineOwnheim';
import { generateCodeowners } from '../src/generateCodeowners.ts';
import type { ResolvedOwnership } from '@ownheim/core/types';

const config = defineOwnheim({
  fallback: 'Platform',
  teams: {
    Billing: { github: '@org/billing', owns: ['packages/billing/**'] },
    Identity: { github: '@org/identity', owns: ['packages/auth/**'] },
    Platform: {
      github: '@org/platform',
      owns: ['packages/billing/admin/**'],
    },
  },
});

describe('generateCodeowners', () => {
  it('emits header, ordered rules, fallback', () => {
    const out = generateCodeowners({ config, resolved: [] });
    expect(out).toContain('This file is generated by @ownheim/build');
    expect(out).toContain('/packages/billing/ @org/billing');
    expect(out).toContain('/packages/auth/ @org/identity');
    expect(out).toContain('/packages/billing/admin/ @org/platform');
    expect(out).toContain('* @org/platform');
  });

  it('emits fallback before explicit rules so CODEOWNERS last-match wins correctly', () => {
    const out = generateCodeowners({ config, resolved: [] });
    const fallbackIdx = out.indexOf('* @org/platform');
    const billingIdx = out.indexOf('/packages/billing/ @org/billing');
    const adminIdx = out.indexOf('/packages/billing/admin/ @org/platform');
    expect(fallbackIdx).toBeLessThan(billingIdx);
    expect(adminIdx).toBeGreaterThan(billingIdx);
  });

  it('emits per-file overrides for jsdoc-resolved teams', () => {
    const resolved: ResolvedOwnership[] = [
      {
        file: 'packages/billing/charge.ts',
        teams: ['Identity'],
        source: 'jsdoc',
      },
    ];
    const out = generateCodeowners({ config, resolved });
    expect(out).toContain('# File-level overrides from @owner JSDoc');
    expect(out).toContain('/packages/billing/charge.ts @org/identity');
  });

  it('does not emit override section when there are no jsdoc resolutions', () => {
    const out = generateCodeowners({ config, resolved: [] });
    expect(out).not.toContain('File-level overrides');
  });

  it('throws instead of emitting an empty CODEOWNERS owner line', () => {
    const malformed = {
      teams: {
        Billing: { github: '', owns: ['packages/billing/**'] },
      },
    };

    expect(() => generateCodeowners({ config: malformed, resolved: [] })).toThrow(
      /missing a GitHub CODEOWNERS handle/,
    );
  });
});
