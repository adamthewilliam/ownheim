import type { ResolvedOwnership, Team, OwnheimConfig } from '@ownheim/core/types';
import { codeownersRulesFirst, getFallbackTeam, planOwnershipRules } from './ownershipRules.ts';

export interface GenerateCodeownersInput<TTeams extends Record<string, Team>> {
  readonly config: OwnheimConfig<TTeams>;
  readonly resolved: readonly ResolvedOwnership[];
  readonly header?: string;
}

const DEFAULT_HEADER = `# This file is generated by @ownheim/build. Do not edit by hand.
# Source of truth: ownheim.config.ts and per-file @owner JSDoc.
# Run \`ownheim generate\` to regenerate.
`;

export function generateCodeowners<TTeams extends Record<string, Team>>(
  input: GenerateCodeownersInput<TTeams>,
): string {
  const { config, resolved } = input;
  const header = input.header ?? DEFAULT_HEADER;

  const sortedRules = codeownersRulesFirst(planOwnershipRules(config));

  const ruleLines = sortedRules.map((r) => {
    const owners = githubOwners(config, r.teams, r.glob);
    return `${globToCodeownersPath(r.glob)} ${owners}`;
  });

  const overrideLines = resolved
    .filter((r) => r.source === 'jsdoc')
    .map((r) => {
      const owners = githubOwners(config, r.teams, r.file);
      return `/${normalisePath(r.file)} ${owners}`;
    });

  const fallbackTeam = getFallbackTeam(config);
  const fallbackLine = fallbackTeam ? `* ${config.teams[fallbackTeam]?.github}` : undefined;

  const sections: string[] = [header.trimEnd()];

  // CODEOWNERS uses last-match-wins semantics. Emit the fallback first so
  // explicit directory rules and per-file overrides can supersede it.
  if (fallbackLine) {
    sections.push('', '# Fallback', fallbackLine);
  }

  sections.push(
    '',
    '# Directory rules (most specific wins; CODEOWNERS uses last-match)',
    ...ruleLines,
  );

  if (overrideLines.length > 0) {
    sections.push('', '# File-level overrides from @owner JSDoc');
    sections.push(...overrideLines);
  }

  return sections.join('\n') + '\n';
}

function githubOwners<TTeams extends Record<string, Team>>(
  config: OwnheimConfig<TTeams>,
  teamIds: readonly string[],
  context: string,
): string {
  const owners = teamIds.map((id) => {
    const github = config.teams[id]?.github;
    if (typeof github !== 'string' || github.trim() === '') {
      throw new Error(
        `generateCodeowners: team '${id}' for '${context}' is missing a GitHub CODEOWNERS handle`,
      );
    }
    return github;
  });
  return owners.join(' ');
}

function globToCodeownersPath(glob: string): string {
  if (glob === '**') return '*';
  if (glob.endsWith('/**')) return `/${glob.slice(0, -3)}/`;
  if (glob.startsWith('**/')) return glob.slice(2);
  return `/${glob}`;
}

function normalisePath(path: string): string {
  return path.replace(/\\/g, '/').replace(/^\/+/, '');
}
