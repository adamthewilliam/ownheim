import type { ResolvedOwnership, Team, StraysConfig } from '@strays/core/types';
import { compareSpecificity } from './globMatcher.ts';

export interface GenerateCodeownersInput<TTeams extends Record<string, Team>> {
  readonly config: StraysConfig<TTeams>;
  readonly resolved: readonly ResolvedOwnership[];
  readonly header?: string;
}

const DEFAULT_HEADER = `# This file is generated by @strays/build. Do not edit by hand.
# Source of truth: strays.config.ts and per-file @owner JSDoc.
# Run \`strays generate\` to regenerate.
`;

interface InternalRule {
  readonly glob: string;
  readonly teamIds: readonly string[];
}

function flattenRules<TTeams extends Record<string, Team>>(
  config: StraysConfig<TTeams>,
): readonly InternalRule[] {
  const rules: InternalRule[] = [];

  for (const [teamId, team] of Object.entries(config.teams)) {
    if (team.owns) {
      for (const glob of team.owns) {
        rules.push({ glob, teamIds: [teamId] });
      }
    }
  }

  if (config.shared) {
    for (const rule of config.shared) {
      rules.push({ glob: rule.glob, teamIds: rule.owners });
    }
  }

  return rules;
}

export function generateCodeowners<TTeams extends Record<string, Team>>(
  input: GenerateCodeownersInput<TTeams>,
): string {
  const { config, resolved } = input;
  const header = input.header ?? DEFAULT_HEADER;

  const sortedRules = flattenRules(config)
    .slice()
    .sort((a, b) => compareSpecificity(a.glob, b.glob));

  const ruleLines = sortedRules.map((r) => {
    const owners = r.teamIds
      .map((id) => config.teams[id]?.github)
      .filter((g): g is string => typeof g === 'string')
      .join(' ');
    return `${globToCodeownersPath(r.glob)} ${owners}`;
  });

  const overrideLines = resolved
    .filter((r) => r.source === 'jsdoc')
    .map((r) => {
      const owners = r.teams
        .map((id) => config.teams[id]?.github)
        .filter((g): g is string => typeof g === 'string')
        .join(' ');
      return `/${normalisePath(r.file)} ${owners}`;
    });

  const fallbackEntry = Object.entries(config.teams).find(([, team]) => team.fallback);
  const fallbackLine = fallbackEntry
    ? `* ${config.teams[fallbackEntry[0]]?.github}`
    : undefined;

  const sections: string[] = [
    header.trimEnd(),
    '',
    '# Directory rules (most specific wins; CODEOWNERS uses last-match)',
    ...ruleLines,
  ];

  if (overrideLines.length > 0) {
    sections.push('', '# File-level overrides from @owner JSDoc');
    sections.push(...overrideLines);
  }

  if (fallbackLine) {
    sections.push('', '# Fallback', fallbackLine);
  }

  return sections.join('\n') + '\n';
}

function globToCodeownersPath(glob: string): string {
  if (glob === '**') return '*';
  if (glob.endsWith('/**')) return `/${glob.slice(0, -3)}/`;
  if (glob.startsWith('**/')) return glob.slice(2);
  return `/${glob}`;
}

function normalisePath(path: string): string {
  return path.replace(/\\/g, '/').replace(/^\/+/, '');
}
